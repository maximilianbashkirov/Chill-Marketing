def get_posting_times(category: str, is_weekend: bool = False) -> list:
    """Получить лучшее время для постов"""
    times = content_knowledge_base.get(category, {}).get("posting_times", {})
    if is_weekend:
        return times.get("weekend", ["10:00-12:00"])
    return times.get("weekday", ["12:00-14:00", "18:00-21:00"])


